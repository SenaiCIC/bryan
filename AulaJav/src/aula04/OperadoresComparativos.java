
package aula04;

public class OperadoresComparativos {
    public static void main(String[] args) {
        int a=10;
        int b=5;
        int c=5;
        boolean result;
        
        //>Maior
        result=a>b;
        System.out.println(result);
        //<Menor
        result=a<b;
        System.out.println(result);
        //>=Maior ou igual
        result=a>=b;
        System.out.println(result);
        //<=Menor ou igual
        result=b<=c;
        System.out.println(result);
        //==Igual
        result=b==c;
        System.out.println(result);
        //!=Diferente
        result=a!=b;
        System.out.println(result);
        
        
        
    }
    
}
