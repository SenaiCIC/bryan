
package aula04;

public class OperadoresAritimeticos {
    public static void main(String[] args) {
       
        int a =5;
        int b= 2;
        int result;
        
        //Operadores + Soma  
        result=a+b;
        System.out.println("Soma de a e b : "+result);
        //Operadores - Subtração
        result=a-b;
        System.out.println("Subtração de a e b : "+result);
        //Operadores * Multiplicação
        result=a*b;
        System.out.println("Multiplicação entre a e b : "+result); 
        //Operadores / Divisão
        double resultado;
        resultado=(double)a/b;
        System.out.println("Divisão entre a e b : "+resultado);
        //Operadores % Módulo
        result=a%b;
        System.out.println("Módulo de a e b : "+result);
          
    }
 
}
